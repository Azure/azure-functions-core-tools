        if (actions.Length == 0)
        {
            sb.Append(indent).AppendLine("Actions = [],");
            return;
        }

        sb.Append(indent).AppendLine("Actions =");
        sb.Append(indent).AppendLine("[");
        string inner = EmitterHelpers.Indent(depth + 1);
        foreach (BindingActionModelV1 a in actions)
        {
            sb.Append(inner).AppendLine("new BindingActionRefV1");
            sb.Append(inner).AppendLine("{");
            string deeper = EmitterHelpers.Indent(depth + 2);
            AppendString(sb, deeper, "Template", a.Template);
            AppendString(sb, deeper, "Binding", a.Binding);
            AppendStringArray(sb, deeper, "Settings", a.Settings);
            sb.Append(inner).AppendLine("},");
        }

        sb.Append(indent).AppendLine("],");
    }

    private static void EmitExtension(StringBuilder sb, BindingExtensionRefModelV1? extension, int depth)
    {
        if (extension is null)
        {
            return;
        }

        string indent = EmitterHelpers.Indent(depth);
        sb.Append(indent).Append("Extension = new BindingExtensionRefV1 { Id = ").Append(EmitterHelpers.Literal(extension.Id))
            .Append(", Version = ").Append(EmitterHelpers.Literal(extension.Version)).AppendLine(" },");
    }

    private static void EmitRules(StringBuilder sb, EquatableArray<BindingRuleModelV1> rules, int depth)
    {
        string indent = EmitterHelpers.Indent(depth);

        if (rules.Length == 0)
        {
            sb.Append(indent).AppendLine("Rules = [],");
            return;
        }

        sb.Append(indent).AppendLine("Rules =");
        sb.Append(indent).AppendLine("[");
        string inner = EmitterHelpers.Indent(depth + 1);
        foreach (BindingRuleModelV1 r in rules)
        {
            sb.Append(inner).AppendLine("new BindingRuleV1");
            sb.Append(inner).AppendLine("{");
            string deeper = EmitterHelpers.Indent(depth + 2);
            AppendString(sb, deeper, "Name", r.Name);
            AppendString(sb, deeper, "Type", r.Type);
            AppendOptionalString(sb, deeper, "Label", r.Label);
            AppendOptionalString(sb, deeper, "Help", r.Help);
            EmitRuleValues(sb, r.Values, depth + 2);
            sb.Append(inner).AppendLine("},");
        }

        sb.Append(indent).AppendLine("],");
    }

    private static void EmitRuleValues(StringBuilder sb, EquatableArray<BindingRuleValueModelV1> values, int depth)
    {
        string indent = EmitterHelpers.Indent(depth);

        if (values.Length == 0)
        {
            sb.Append(indent).AppendLine("Values = [],");
            return;
        }

        sb.Append(indent).AppendLine("Values =");
        sb.Append(indent).AppendLine("[");
        string inner = EmitterHelpers.Indent(depth + 1);
        foreach (BindingRuleValueModelV1 v in values)
        {
            sb.Append(inner).AppendLine("new BindingRuleValueV1");
            sb.Append(inner).AppendLine("{");
            string deeper = EmitterHelpers.Indent(depth + 2);
            AppendString(sb, deeper, "Value", v.Value);
            AppendString(sb, deeper, "Display", v.Display);
            AppendStringArray(sb, deeper, "HiddenSettings", v.HiddenSettings);
            AppendStringArray(sb, deeper, "ShownSettings", v.ShownSettings);
            sb.Append(inner).AppendLine("},");
        }

        sb.Append(indent).AppendLine("],");
    }

    private static void AppendString(StringBuilder sb, string indent, string name, string value)
    {
        sb.Append(indent).Append(name).Append(" = ").Append(EmitterHelpers.Literal(value)).AppendLine(",");
    }

    private static void AppendOptionalString(StringBuilder sb, string indent, string name, string? value)
    {
        if (value is not null)
        {
            AppendString(sb, indent, name, value);
        }
    }

    private static void AppendBool(StringBuilder sb, string indent, string name, bool value)
    {
        sb.Append(indent).Append(name).Append(" = ").Append(value ? "true" : "false").AppendLine(",");
    }

    private static void AppendStringArray(StringBuilder sb, string indent, string name, EquatableArray<string> values)
    {
        sb.Append(indent).Append(name).Append(" = [");
        for (int i = 0; i < values.Length; i++)
        {
            if (i > 0)
            {
                sb.Append(", ");
            }

            sb.Append(EmitterHelpers.Literal(values[i]));
        }

        sb.AppendLine("],");
    }
