    public static IEnumerable<(string HintName, string Source)> EmitStaticData(BindingsCatalogModelV1 catalog)
    {
        // Per-binding file: KnownBindings.V1.{Name}.g.cs
        foreach (BindingDefinitionModelV1 binding in catalog.Bindings)
        {
            var sb = new StringBuilder(4096);
            AppendHeader(sb);
            sb.AppendLine("internal static partial class KnownBindings");
            sb.AppendLine("{");
            sb.AppendLine("    internal static partial class V1");
            sb.AppendLine("    {");

            string fieldName = ToFieldName(binding.Type, binding.Direction);
            sb.Append("        public static readonly BindingDefinitionV1 ").Append(fieldName).AppendLine(" =");
            EmitBinding(sb, binding, 3);
            sb.AppendLine(";");

            sb.AppendLine("    }");
            sb.AppendLine("}");
            yield return ("KnownBindings.V1." + fieldName + ".g.cs", sb.ToString());
        }

        // Aggregate: KnownBindings.V1.g.cs
        {
            var sb = new StringBuilder(2048);
            AppendHeader(sb);
            sb.AppendLine("internal static partial class KnownBindings");
            sb.AppendLine("{");
            sb.AppendLine("    internal static partial class V1");
            sb.AppendLine("    {");

            sb.AppendLine("        public static readonly global::System.Collections.Generic.IReadOnlyDictionary<string, BindingDefinitionV1> All =");
            sb.AppendLine("            new global::System.Collections.Generic.Dictionary<string, BindingDefinitionV1>");
            sb.AppendLine("            {");
            foreach (BindingDefinitionModelV1 binding in catalog.Bindings)
            {
                string fieldName = ToFieldName(binding.Type, binding.Direction);
                string key = ToCatalogKey(binding.Type, binding.Direction);
                sb.Append("                [").Append(EmitterHelpers.Literal(key)).Append("] = ").Append(fieldName).AppendLine(",");
            }

            sb.AppendLine("            };");
            sb.AppendLine();

            sb.AppendLine("        public static readonly global::System.Collections.Generic.IReadOnlyDictionary<string, string> Variables =");
            sb.AppendLine("            new global::System.Collections.Generic.Dictionary<string, string>");
            sb.AppendLine("            {");
            foreach (VariableEntryModelV1 variable in catalog.Variables)
            {
                sb.Append("                [").Append(EmitterHelpers.Literal(variable.Name)).Append("] = ").Append(EmitterHelpers.Literal(variable.Value)).AppendLine(",");
            }

            sb.AppendLine("            };");
            sb.AppendLine("    }");
            sb.AppendLine("}");
            yield return ("KnownBindings.V1.g.cs", sb.ToString());
        }
    }

    private static void AppendHeader(StringBuilder sb)
    {
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.Append("namespace ").Append(BindingsNamespace).AppendLine(";");
        sb.AppendLine();
    }

    private static string ToFieldName(string type, BindingDirectionKindV1 direction)
    {
        string sanitized = EmitterHelpers.SanitizeIdentifier(type);
        if (sanitized.Length == 0)
        {
            return sanitized;
        }

        if (char.IsLower(sanitized[0]))
        {
            sanitized = char.ToUpperInvariant(sanitized[0]) + sanitized.Substring(1);
        }

        // Trigger types already end in "Trigger" by convention; appending the direction would
        // produce awkward names like "BlobTriggerTrigger". Only suffix In/Out, where the same
        // binding type appears twice (once per direction) in bindings.json.
        return direction switch
        {
            BindingDirectionKindV1.In => sanitized + "In",
            BindingDirectionKindV1.Out => sanitized + "Out",
            BindingDirectionKindV1.Trigger => sanitized,
            _ => sanitized,
        };
    }

    // Stable, parseable lookup key: "{type}.{direction}" (e.g. "blob.in", "queueTrigger.trigger").
    private static string ToCatalogKey(string type, BindingDirectionKindV1 direction)
    {
        string dir = direction switch
        {
            BindingDirectionKindV1.In => "in",
            BindingDirectionKindV1.Out => "out",
            BindingDirectionKindV1.Trigger => "trigger",
            _ => "in",
        };

        return type + "." + dir;
    }

    private static void EmitBinding(StringBuilder sb, BindingDefinitionModelV1 binding, int depth)
    {
        string indent = EmitterHelpers.Indent(depth);
        string inner = EmitterHelpers.Indent(depth + 1);

        sb.Append(indent).AppendLine("new BindingDefinitionV1");
        sb.Append(indent).AppendLine("{");
        AppendString(sb, inner, "Type", binding.Type);
        AppendString(sb, inner, "DisplayName", binding.DisplayName);
        sb.Append(inner).Append("Direction = BindingSchemaDirectionV1.").Append(binding.Direction switch
        {
            BindingDirectionKindV1.In => "In",
            BindingDirectionKindV1.Out => "Out",
            BindingDirectionKindV1.Trigger => "Trigger",
            _ => "In",
        }).AppendLine(",");
        AppendBool(sb, inner, "EnabledInTryMode", binding.EnabledInTryMode);
        AppendOptionalString(sb, inner, "Documentation", binding.Documentation);
        EmitSettings(sb, binding.Settings, depth + 1);
        EmitActions(sb, binding.Actions, depth + 1);
        EmitExtension(sb, binding.Extension, depth + 1);
        EmitRules(sb, binding.Rules, depth + 1);
        sb.Append(indent).Append('}');
    }

    private static void EmitSettings(StringBuilder sb, EquatableArray<BindingSettingModelV1> settings, int depth)
    {
        string indent = EmitterHelpers.Indent(depth);

        if (settings.Length == 0)
        {
            sb.Append(indent).AppendLine("Settings = [],");
            return;
        }

        sb.Append(indent).AppendLine("Settings =");
        sb.Append(indent).AppendLine("[");
        foreach (BindingSettingModelV1 s in settings)
        {
            EmitSetting(sb, s, depth + 1);
            sb.AppendLine(",");
        }

        sb.Append(indent).AppendLine("],");
    }

    private static void EmitSetting(StringBuilder sb, BindingSettingModelV1 setting, int depth)
    {
        string indent = EmitterHelpers.Indent(depth);
        string inner = EmitterHelpers.Indent(depth + 1);

        sb.Append(indent).AppendLine("new BindingSettingV1");
        sb.Append(indent).AppendLine("{");
        AppendString(sb, inner, "Name", setting.Name);
        sb.Append(inner).Append("Value = BindingSettingValueTypeV1.").Append(setting.Value switch
        {
            BindingSettingValueKindV1.String => "String",
            BindingSettingValueKindV1.Bool => "Bool",
            BindingSettingValueKindV1.Int => "Int",
            BindingSettingValueKindV1.Enum => "Enum",
            BindingSettingValueKindV1.CheckBoxList => "CheckBoxList",
            _ => "String",
        }).AppendLine(",");

        if (setting.Required.HasValue)
        {
            AppendBool(sb, inner, "Required", setting.Required.Value);
        }

        AppendOptionalString(sb, inner, "Label", setting.Label);
        AppendOptionalString(sb, inner, "Help", setting.Help);

        if (setting.DefaultValue is not null)
        {
            sb.Append(inner).Append("DefaultValue = ");
            EmitDefaultValue(sb, setting.DefaultValue);
            sb.AppendLine(",");
        }

        AppendOptionalString(sb, inner, "Placeholder", setting.Placeholder);
        AppendOptionalString(sb, inner, "Resource", setting.Resource);

        EmitEnumValues(sb, setting.Enum, depth + 1);
        EmitValidators(sb, setting.Validators, depth + 1);

        sb.Append(indent).Append('}');
    }

    private static void EmitDefaultValue(StringBuilder sb, SettingDefaultValueModelV1 value)
    {
        switch (value.Kind)
        {
            case SettingDefaultValueKindV1.String:
                sb.Append(value.StringValue is null ? "null" : EmitterHelpers.Literal(value.StringValue));
                break;
            case SettingDefaultValueKindV1.Bool:
                sb.Append(value.BoolValue == true ? "true" : "false");
                break;
            case SettingDefaultValueKindV1.Long:
                sb.Append((value.LongValue ?? 0L).ToString(CultureInfo.InvariantCulture)).Append('L');
                break;
            case SettingDefaultValueKindV1.StringArray:
                sb.Append("new string[] { ");
                if (value.ArrayValue.HasValue)
                {
                    for (int i = 0; i < value.ArrayValue.Value.Length; i++)
                    {
                        if (i > 0)
                        {
                            sb.Append(", ");
                        }

                        sb.Append(EmitterHelpers.Literal(value.ArrayValue.Value[i]));
                    }
                }

                sb.Append(" }");
                break;
            default:
                sb.Append("null");
                break;
        }
    }

    private static void EmitEnumValues(StringBuilder sb, EquatableArray<BindingEnumValueModelV1> values, int depth)
    {
