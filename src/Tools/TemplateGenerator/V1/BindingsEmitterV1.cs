        if (values.Length == 0)
        {
            sb.Append(indent).AppendLine("Enum = [],");
            return;
        }

        sb.Append(indent).AppendLine("Enum =");
        sb.Append(indent).AppendLine("[");
        string inner = EmitterHelpers.Indent(depth + 1);
        foreach (BindingEnumValueModelV1 v in values)
        {
            sb.Append(inner).Append("new BindingEnumValueV1 { Value = ").Append(EmitterHelpers.Literal(v.Value))
                .Append(", Display = ").Append(EmitterHelpers.Literal(v.Display)).AppendLine(" },");
        }

        sb.Append(indent).AppendLine("],");
    }

